CREATE OR REPLACE FUNCTION dna_memory.update_central_memory_occ(
  p_tenant_id TEXT,
  p_user_id TEXT,
  p_id BIGINT,
  p_expected_version INTEGER,
  p_content TEXT DEFAULT NULL,
  p_metadata_jsonb JSONB DEFAULT NULL,
  p_token_count INTEGER DEFAULT NULL,
  p_last_accessed_at TIMESTAMPTZ DEFAULT NULL
)
RETURNS TABLE (applied BOOLEAN, new_version INTEGER)
LANGUAGE plpgsql
AS $$
DECLARE
  v_next_version INTEGER;
BEGIN
  UPDATE dna_memory.central_memory
  SET
    content = COALESCE(p_content, content),
    metadata_jsonb = COALESCE(p_metadata_jsonb, metadata_jsonb),
    token_count = COALESCE(p_token_count, token_count),
    last_accessed_at = COALESCE(p_last_accessed_at, last_accessed_at),
    version = version + 1,
    updated_at = NOW()
  WHERE tenant_id = p_tenant_id
    AND user_id = p_user_id
    AND id = p_id
    AND version = p_expected_version
  RETURNING version INTO v_next_version;

  IF v_next_version IS NULL THEN
    RETURN QUERY SELECT FALSE, NULL::INTEGER;
  ELSE
    RETURN QUERY SELECT TRUE, v_next_version;
  END IF;
END;
$$;

CREATE OR REPLACE FUNCTION dna_memory.traverse_graph(
  p_tenant_id TEXT,
  p_user_id TEXT,
  p_start_node_id BIGINT,
  p_max_depth INTEGER DEFAULT 3
)
RETURNS TABLE (
  depth INTEGER,
  source_id BIGINT,
  target_id BIGINT,
  relationship_type TEXT,
  weight DOUBLE PRECISION,
  path BIGINT[]
)
LANGUAGE SQL
STABLE
AS $$
  WITH RECURSIVE walk AS (
    SELECT
      1 AS depth,
      e.source_id,
      e.target_id,
      e.relationship_type,
      e.weight,
      ARRAY[e.source_id, e.target_id]::BIGINT[] AS path
    FROM dna_memory.edges e
    WHERE e.tenant_id = p_tenant_id
      AND e.user_id = p_user_id
      AND e.source_id = p_start_node_id

    UNION ALL

    SELECT
      walk.depth + 1,
      e.source_id,
      e.target_id,
      e.relationship_type,
      e.weight,
      walk.path || e.target_id
    FROM walk
    JOIN dna_memory.edges e
      ON e.tenant_id = p_tenant_id
      AND e.user_id = p_user_id
      AND e.source_id = walk.target_id
    WHERE walk.depth < GREATEST(1, p_max_depth)
      AND NOT (e.target_id = ANY(walk.path))
  )
  SELECT depth, source_id, target_id, relationship_type, weight, path
  FROM walk
  ORDER BY depth ASC, weight DESC;
$$;

