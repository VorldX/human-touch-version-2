CREATE SCHEMA IF NOT EXISTS dna_memory;

DO $$
BEGIN
  CREATE TYPE dna_memory.memory_tier AS ENUM ('LONG_TERM', 'ARCHIVE', 'STAGING');
EXCEPTION
  WHEN duplicate_object THEN NULL;
END;
$$;

DO $$
BEGIN
  CREATE TYPE dna_memory.memory_domain AS ENUM ('CONTEXTUAL', 'WORKING');
EXCEPTION
  WHEN duplicate_object THEN NULL;
END;
$$;

DO $$
BEGIN
  CREATE TYPE dna_memory.memory_kind AS ENUM ('FACT', 'GRAPH_NODE', 'RULE', 'SOP_PATHWAY');
EXCEPTION
  WHEN duplicate_object THEN NULL;
END;
$$;

CREATE TABLE IF NOT EXISTS dna_memory.central_memory (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  tenant_id TEXT NOT NULL,
  user_id TEXT NOT NULL,
  tier dna_memory.memory_tier NOT NULL,
  memory_domain dna_memory.memory_domain NOT NULL,
  memory_kind dna_memory.memory_kind NOT NULL,
  document_id TEXT NOT NULL,
  chunk_index INTEGER NOT NULL CHECK (chunk_index >= 0),
  token_count INTEGER NOT NULL CHECK (token_count >= 0),
  content TEXT NOT NULL,
  embedding vector(512) NOT NULL,
  metadata_jsonb JSONB NOT NULL DEFAULT '{}'::jsonb,
  schema_version TEXT NOT NULL,
  version INTEGER NOT NULL DEFAULT 1 CHECK (version > 0),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  last_accessed_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT central_memory_pk PRIMARY KEY (tenant_id, user_id, id),
  CONSTRAINT central_memory_doc_chunk_unique UNIQUE (tenant_id, user_id, tier, document_id, chunk_index),
  CONSTRAINT central_memory_domain_kind_check CHECK (
    (
      memory_domain = 'CONTEXTUAL'::dna_memory.memory_domain
      AND memory_kind IN ('FACT'::dna_memory.memory_kind, 'GRAPH_NODE'::dna_memory.memory_kind)
    )
    OR
    (
      memory_domain = 'WORKING'::dna_memory.memory_domain
      AND memory_kind IN ('RULE'::dna_memory.memory_kind, 'SOP_PATHWAY'::dna_memory.memory_kind)
    )
  )
) PARTITION BY LIST (tenant_id);

CREATE TABLE IF NOT EXISTS dna_memory.central_memory_p_default
  PARTITION OF dna_memory.central_memory DEFAULT;

CREATE TABLE IF NOT EXISTS dna_memory.nodes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  label TEXT NOT NULL,
  properties_jsonb JSONB NOT NULL DEFAULT '{}'::jsonb,
  tenant_id TEXT NOT NULL,
  user_id TEXT NOT NULL,
  schema_version TEXT NOT NULL,
  version INTEGER NOT NULL DEFAULT 1 CHECK (version > 0),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT nodes_pk PRIMARY KEY (tenant_id, user_id, id)
) PARTITION BY LIST (tenant_id);

CREATE TABLE IF NOT EXISTS dna_memory.nodes_p_default
  PARTITION OF dna_memory.nodes DEFAULT;

CREATE TABLE IF NOT EXISTS dna_memory.edges (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  source_id BIGINT NOT NULL,
  target_id BIGINT NOT NULL,
  relationship_type TEXT NOT NULL,
  weight DOUBLE PRECISION NOT NULL DEFAULT 1 CHECK (weight >= 0),
  tenant_id TEXT NOT NULL,
  user_id TEXT NOT NULL,
  schema_version TEXT NOT NULL,
  version INTEGER NOT NULL DEFAULT 1 CHECK (version > 0),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT edges_pk PRIMARY KEY (tenant_id, user_id, id),
  CONSTRAINT edges_source_fk
    FOREIGN KEY (tenant_id, user_id, source_id)
    REFERENCES dna_memory.nodes (tenant_id, user_id, id)
    ON DELETE CASCADE,
  CONSTRAINT edges_target_fk
    FOREIGN KEY (tenant_id, user_id, target_id)
    REFERENCES dna_memory.nodes (tenant_id, user_id, id)
    ON DELETE CASCADE
) PARTITION BY LIST (tenant_id);

CREATE TABLE IF NOT EXISTS dna_memory.edges_p_default
  PARTITION OF dna_memory.edges DEFAULT;

CREATE OR REPLACE FUNCTION dna_memory.partition_suffix(p_tenant_id TEXT, p_user_id TEXT)
RETURNS TEXT
LANGUAGE SQL
IMMUTABLE
AS $$
  SELECT SUBSTRING(
    md5(lower(trim(COALESCE(p_tenant_id, ''))))
    FROM 1 FOR 20
  );
$$;

CREATE OR REPLACE FUNCTION dna_memory.ensure_vector_index(p_index_name TEXT, p_table REGCLASS)
RETURNS VOID
LANGUAGE plpgsql
AS $$
BEGIN
  BEGIN
    EXECUTE format(
      'CREATE INDEX IF NOT EXISTS %I ON %s USING hnsw (embedding vector_cosine_ops) WITH (m = 16, ef_construction = 64)',
      p_index_name,
      p_table
    );
  EXCEPTION
    WHEN undefined_object OR feature_not_supported THEN
      EXECUTE format(
        'CREATE INDEX IF NOT EXISTS %I ON %s USING ivfflat (embedding vector_cosine_ops) WITH (lists = 64)',
        p_index_name,
        p_table
      );
  END;
END;
$$;

CREATE OR REPLACE FUNCTION dna_memory.ensure_partition_for_subject(
  p_tenant_id TEXT,
  p_user_id TEXT
)
RETURNS TABLE (central_partition TEXT, nodes_partition TEXT, edges_partition TEXT)
LANGUAGE plpgsql
AS $$
DECLARE
  suffix TEXT;
  central_name TEXT;
  nodes_name TEXT;
  edges_name TEXT;
BEGIN
  IF trim(COALESCE(p_tenant_id, '')) = '' OR trim(COALESCE(p_user_id, '')) = '' THEN
    RAISE EXCEPTION 'tenant_id and user_id must be non-empty for partition provisioning';
  END IF;

  suffix := dna_memory.partition_suffix(p_tenant_id, p_user_id);
  central_name := format('central_memory_p_%s', suffix);
  nodes_name := format('nodes_p_%s', suffix);
  edges_name := format('edges_p_%s', suffix);

  IF to_regclass(format('dna_memory.%I', central_name)) IS NULL THEN
    EXECUTE format(
      'CREATE TABLE dna_memory.%I PARTITION OF dna_memory.central_memory FOR VALUES IN (%L)',
      central_name,
      p_tenant_id
    );
  END IF;

  IF to_regclass(format('dna_memory.%I', nodes_name)) IS NULL THEN
    EXECUTE format(
      'CREATE TABLE dna_memory.%I PARTITION OF dna_memory.nodes FOR VALUES IN (%L)',
      nodes_name,
      p_tenant_id
    );
  END IF;

  IF to_regclass(format('dna_memory.%I', edges_name)) IS NULL THEN
    EXECUTE format(
      'CREATE TABLE dna_memory.%I PARTITION OF dna_memory.edges FOR VALUES IN (%L)',
      edges_name,
      p_tenant_id
    );
  END IF;

  PERFORM dna_memory.ensure_vector_index(
    format('%s_embedding_idx', central_name),
    format('dna_memory.%I', central_name)::regclass
  );

  EXECUTE format(
    'CREATE INDEX IF NOT EXISTS %I ON dna_memory.%I (tier, memory_domain, updated_at DESC)',
    format('%s_tier_domain_updated_idx', central_name),
    central_name
  );

  EXECUTE format(
    'CREATE INDEX IF NOT EXISTS %I ON dna_memory.%I (document_id, chunk_index)',
    format('%s_doc_chunk_idx', central_name),
    central_name
  );

  EXECUTE format(
    'CREATE INDEX IF NOT EXISTS %I ON dna_memory.%I (label)',
    format('%s_label_idx', nodes_name),
    nodes_name
  );

  EXECUTE format(
    'CREATE INDEX IF NOT EXISTS %I ON dna_memory.%I (source_id, relationship_type, weight DESC)',
    format('%s_source_rel_idx', edges_name),
    edges_name
  );

  EXECUTE format(
    'CREATE INDEX IF NOT EXISTS %I ON dna_memory.%I (target_id)',
    format('%s_target_idx', edges_name),
    edges_name
  );

  RETURN QUERY SELECT central_name, nodes_name, edges_name;
END;
$$;

CREATE OR REPLACE FUNCTION dna_memory.touch_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at := NOW();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_central_memory_touch_updated_at ON dna_memory.central_memory;
CREATE TRIGGER trg_central_memory_touch_updated_at
BEFORE UPDATE ON dna_memory.central_memory
FOR EACH ROW
EXECUTE FUNCTION dna_memory.touch_updated_at();

