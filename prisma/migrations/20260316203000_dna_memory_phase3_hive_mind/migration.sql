CREATE OR REPLACE VIEW dna_memory.phase3_blackboard_overview AS
SELECT
  s.tenant_id,
  s.user_id,
  s.board_id,
  s.pathway_id,
  s.session_id,
  s.status AS board_status,
  s.created_at,
  COUNT(st.id)::bigint AS total_steps,
  COUNT(st.id) FILTER (WHERE st.status = 'PENDING'::dna_memory.blackboard_step_status)::bigint AS pending_steps,
  COUNT(st.id) FILTER (WHERE st.status IN ('CLAIMED'::dna_memory.blackboard_step_status, 'IN_PROGRESS'::dna_memory.blackboard_step_status))::bigint AS claimed_steps,
  COUNT(st.id) FILTER (WHERE st.status = 'COMPLETED'::dna_memory.blackboard_step_status)::bigint AS completed_steps
FROM dna_memory.blackboard_sessions s
LEFT JOIN dna_memory.blackboard_steps st
  ON st.tenant_id = s.tenant_id
  AND st.user_id = s.user_id
  AND st.board_id = s.board_id
GROUP BY
  s.tenant_id,
  s.user_id,
  s.board_id,
  s.pathway_id,
  s.session_id,
  s.status,
  s.created_at;
