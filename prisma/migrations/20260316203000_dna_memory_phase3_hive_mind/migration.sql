CREATE OR REPLACE FUNCTION dna_memory.ensure_phase3_partition_for_subject(
  p_tenant_id TEXT,
  p_user_id TEXT
)
RETURNS TABLE (
  pathway_partition TEXT,
  board_partition TEXT,
  step_partition TEXT,
  sync_partition TEXT
)
LANGUAGE plpgsql
AS $$
DECLARE
  suffix TEXT;
  pathway_name TEXT;
  board_name TEXT;
  step_name TEXT;
  sync_name TEXT;
BEGIN
  IF trim(COALESCE(p_tenant_id, '')) = '' OR trim(COALESCE(p_user_id, '')) = '' THEN
    RAISE EXCEPTION 'tenant_id and user_id must be non-empty for phase3 partition provisioning';
  END IF;

  suffix := dna_memory.partition_suffix(p_tenant_id, p_user_id);
  pathway_name := format('pathway_registry_p_%s', suffix);
  board_name := format('blackboard_sessions_p_%s', suffix);
  step_name := format('blackboard_steps_p_%s', suffix);
  sync_name := format('dna_sync_events_p_%s', suffix);

  IF to_regclass(format('dna_memory.%I', pathway_name)) IS NULL THEN
    EXECUTE format(
      'CREATE TABLE dna_memory.%I PARTITION OF dna_memory.pathway_registry FOR VALUES IN (%L)',
      pathway_name,
      p_tenant_id
    );
  END IF;

  IF to_regclass(format('dna_memory.%I', board_name)) IS NULL THEN
    EXECUTE format(
      'CREATE TABLE dna_memory.%I PARTITION OF dna_memory.blackboard_sessions FOR VALUES IN (%L)',
      board_name,
      p_tenant_id
    );
  END IF;

  IF to_regclass(format('dna_memory.%I', step_name)) IS NULL THEN
    EXECUTE format(
      'CREATE TABLE dna_memory.%I PARTITION OF dna_memory.blackboard_steps FOR VALUES IN (%L)',
      step_name,
      p_tenant_id
    );
  END IF;

  IF to_regclass(format('dna_memory.%I', sync_name)) IS NULL THEN
    EXECUTE format(
      'CREATE TABLE dna_memory.%I PARTITION OF dna_memory.dna_sync_events FOR VALUES IN (%L)',
      sync_name,
      p_tenant_id
    );
  END IF;

  EXECUTE format(
    'CREATE INDEX IF NOT EXISTS %I ON dna_memory.%I (status, pathway_name)',
    format('%s_status_name_idx', pathway_name),
    pathway_name
  );

  EXECUTE format(
    'CREATE INDEX IF NOT EXISTS %I ON dna_memory.%I (status, created_at DESC)',
    format('%s_status_created_idx', board_name),
    board_name
  );

  EXECUTE format(
    'CREATE INDEX IF NOT EXISTS %I ON dna_memory.%I (board_id, step_order ASC)',
    format('%s_board_order_idx', step_name),
    step_name
  );

  EXECUTE format(
    'CREATE INDEX IF NOT EXISTS %I ON dna_memory.%I (event_type, created_at DESC)',
    format('%s_event_created_idx', sync_name),
    sync_name
  );

  RETURN QUERY SELECT pathway_name, board_name, step_name, sync_name;
END;
$$;

CREATE OR REPLACE FUNCTION dna_memory.update_pathway_registry_occ(
  p_tenant_id TEXT,
  p_user_id TEXT,
  p_id BIGINT,
  p_expected_version INTEGER,
  p_pathway_jsonb JSONB DEFAULT NULL,
  p_status dna_memory.pathway_status DEFAULT NULL,
  p_metadata_jsonb JSONB DEFAULT NULL,
  p_overrides_pathway_id UUID DEFAULT NULL
)
RETURNS TABLE (applied BOOLEAN, new_version INTEGER)
LANGUAGE plpgsql
AS $$
DECLARE
  v_next_version INTEGER;
BEGIN
  UPDATE dna_memory.pathway_registry
  SET
    pathway_jsonb = COALESCE(p_pathway_jsonb, pathway_jsonb),
    status = COALESCE(p_status, status),
    metadata_jsonb = COALESCE(p_metadata_jsonb, metadata_jsonb),
    overrides_pathway_id = COALESCE(p_overrides_pathway_id, overrides_pathway_id),
    version = version + 1,
    updated_at = NOW()
  WHERE tenant_id = p_tenant_id
    AND user_id = p_user_id
    AND id = p_id
    AND version = p_expected_version
  RETURNING version INTO v_next_version;

  IF v_next_version IS NULL THEN
    RETURN QUERY SELECT FALSE, NULL::INTEGER;
  ELSE
    RETURN QUERY SELECT TRUE, v_next_version;
  END IF;
END;
$$;

