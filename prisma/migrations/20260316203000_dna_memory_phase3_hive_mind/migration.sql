CREATE TABLE IF NOT EXISTS dna_memory.pathway_registry (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  pathway_id UUID NOT NULL DEFAULT gen_random_uuid(),
  tenant_id TEXT NOT NULL,
  user_id TEXT NOT NULL,
  pathway_name TEXT NOT NULL,
  pathway_jsonb JSONB NOT NULL DEFAULT '{}'::jsonb,
  pathway_hash TEXT NOT NULL,
  status dna_memory.pathway_status NOT NULL DEFAULT 'ACTIVE',
  overrides_pathway_id UUID,
  schema_version TEXT NOT NULL,
  version INTEGER NOT NULL DEFAULT 1 CHECK (version > 0),
  metadata_jsonb JSONB NOT NULL DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT pathway_registry_pk PRIMARY KEY (tenant_id, user_id, id),
  CONSTRAINT pathway_registry_pathway_unique UNIQUE (tenant_id, user_id, pathway_id)
) PARTITION BY LIST (tenant_id);

